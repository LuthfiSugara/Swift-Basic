import Foundation

//Logical AND
let enterCode = true
let scanCode = false

if enterCode && scanCode {
    print("True")
}else{
    print("False")
}

//Logical OR
if enterCode || scanCode{
    print("True")
}else{
    print("False")
}

//Logical NOT
if !enterCode{
    print("True")
}else{
    print("False")
}

